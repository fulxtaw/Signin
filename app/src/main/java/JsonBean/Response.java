public class Response {
    private String msg;
    private String id;

    public Response() {
    }

    public  String getMsg() {
        return msg;
    }

    public  void setMsg(String lmessage) {
        msg = lmessage;
    }

    public  String getId() {
        return id;
    }

    public  void setId(String lid) {
        id = lid;
    }
}
